public class ClaseMathExamples {
    public static void main(String[] args) {
        int absoluto = Math.abs(-3);
        System.out.println("absoluto = " + absoluto);

        double max = Math.max(3.5, 1.3);

        System.out.println("max = " + max);

        double min = Math.min(15, 27);
        System.out.println("min = " + min);

        double techo = Math.ceil(3.5);
        System.out.println("techo = " + techo);

        double piso = Math.floor(15.3);

        System.out.println("piso = " + piso);

        double entero = Math.round(3.865);

        System.out.println("entero = " + entero);

        double exponential = Math.exp(2);
        System.out.println("exponential = " + exponential);

        double log = Math.log(10);

        System.out.println("log = " + log);

        double potencia = Math.pow(10,3);

        System.out.println("potencia = " + potencia);

        double raiz = Math.sqrt(25);

        System.out.println("raiz = " + raiz);

    }
}
